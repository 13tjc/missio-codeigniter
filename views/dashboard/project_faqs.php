                            <li>Have a tone that is reflective of who you are.</li>
                            <li>Tell the back-story of your project and your journey until this point.</li>
                            <li>Touch on "why this project matters".</li>
                            <li>Share details on where the money is going and what you are trying to accomplish.</li>
